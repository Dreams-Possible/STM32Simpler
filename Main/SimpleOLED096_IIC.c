//固定位置显示字符串
//位置：行数：1-8；列数：1-128，字符串
void OLED096_CharacterString_IIC(uint8_t Axle_A,uint8_t Axle_B,char*CharacterString,...)
{
	--Axle_A;
	--Axle_B;
	//最大容纳32个字符，实际限制为16个字符
    uint8_t StringData[32]=
    		{
    				0,
    		};

    //使用可变参数列表
    va_list args;
    va_start(args,CharacterString);

    //将参数传递给sprintf函数
    vsprintf((char*)StringData,(char*)CharacterString,args);

    //结束可变参数列表
    va_end(args);

    //扫描连续两行，每行高8像素
    for(uint8_t a=0;a<2;++a)
	{
    	//显示焦点定位
		OLED096_Focus_IIC(Axle_A+a,Axle_B);
		//分别发送字符串的上下两部分
		for(uint8_t b=0;b<strlen((char*)StringData);++b)
		{
			//发送一个字的宽度
			for(uint8_t c=0;c<8;++c)
			{
				OLED096_Data_IIC(OLED096_Character_ASCII_1608[StringData[b]-' '][c+a*8]);
			}
		}
	}
}
#endif//OLED096_Mode_CharacterString




//Printf模式
#ifdef OLED096_Mode_Printf


void OLED096_Printf_IIC(const char *CharacterString,...)
{
	static uint8_t Axle_A=0;
	static uint8_t Axle_B=0;

	uint8_t StringData[32];//最大容纳32个字符，实际限制为16个字符
	//使用可变参数列表
	va_list args;
	va_start(args,CharacterString);
	//将参数传递给sprintf函数
	vsprintf((char*)StringData,(char*)CharacterString,args);
	//结束可变参数列表
	va_end(args);

	//用空格填充一行
	uint8_t CharacterNumber=strlen((char*)StringData);
	for(uint8_t a=CharacterNumber;a<16;++a)
	{
		StringData[a]=' ';
	}
	StringData[16]='\0';

	//扫描连续两行，每行高8像素
	for(uint8_t a=0;a<2;++a)
	{
		//显示焦点定位
		OLED096_Focus_IIC(Axle_A+a,Axle_B);
		//分别发送字符串的上下两部分
		for(uint8_t b=0;b<16;++b)
		{
			//发送一个字的宽度
			for(uint8_t c=0;c<8;++c)
			{
				OLED096_Data_IIC(OLED096_Character_ASCII_1608[StringData[b]-' '][c+a*8]);
			}
		}
	}

	if(Axle_A>=8)
	{
		Axle_A=0;
	}else
	{
		Axle_A+=2;
	}

}


#endif//OLED096_Mode_Printf


